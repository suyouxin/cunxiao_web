   <td colspan="4"><!-- InstanceBeginEditable name="EditRegion3" -->
     <p>在我们看不见的乡村地带，有许多孩子，他们和我们自己的孩子一样，生机勃勃，充满希望，却无奈被不公命运带来的贫困之网束缚。现在，我们有机会和她们一起，并给她们一些小小的帮助。如果你已准备好，要帮助其中的一个或几个，请仔细阅读下文：</p>
     <p><span class="STYLE1">1、帮助一个小学生。( 走读-每人每学期 / 200人民币 ，住校生-每人每学期 / 200-800人民币)</span><br />
      九年义务教育已落实到村里，学费免除，杂费少许。地区不同，收费不同，以陕南的小学09年上半学期为例，一年级收二十几块，六年级收八十几块，包括保险费、资料费等。如无因家远住校产生的食宿费，一般家庭是完全可承受的。所以资助小学生，我们的出发点是社会关怀。这些孩子大多来自破碎家庭，也许父亲去世，也许母亲出走，或家长残疾失去劳力，还有来自多人口家庭。我们希望将之收集成一个资料库，在他们将来上初中、技校、高中、乃至大学时，给予帮助。我们更看重的是，希望让孩子在童年时即感受到来自他人的关爱，这好比春天里种下一颗种子，迟早都会发芽。</p>
     <p><span class="STYLE1">2、帮助一个初中生。( 每人每学期 / 400-800人民币 )</span><br />
      初中生学校收费一学期一百块左右，因乡村初中都在镇上，真正压力来自住校产生的食宿费。按每月300块标准算，一个初中生一学期至少花去1200元。更因家庭贫困，导致升学无望，初中生中途辍学比例惊人。我们走访的很多学校中，有初一近200个学生，到初三时剩下不到60人的，辍学学生有回家种地，有进城打工，有下矿井，只能做毫无技术含量的苦力活。资助初中生，不但可大大减轻学生家庭压力，还有机会防止其中途辍学，甚至让其有信心进入高中、技校，继续学业，以获更光明人生路。</p>
     <p><span class="STYLE1">3、帮助一个高中生。( 每人每学期 / 800-2000人民币 )</span><br />
      如没有打工等额外收入，一个高中生的开支，几乎压垮一个农民家庭。学费、补课费、资料费、加生活费，每年需高达6000-8000元甚至更多(2012年调查更新)。家长为了筹集孩子的高中学费，从事报酬较高，但极度危险矿井工作已是寻常事，从某种意义上说，资助一个高中生，很可能同时也挽救了一个家庭。为更有效利用社会资源，我们对高中生设置了较高资助门槛，成绩达所在高中升学率一本至二本的中间线，也就是有机会考入大学一本或二本的学生，才会进入我们的资助名单。资助金额上，我们将根据不同的家庭情况，在800元-2000元间浮动。而对于极困家庭，在得到你的捐助之外，还有机会额外得到珠海桦渊基金会的急难救助和无息贷款。</p>
