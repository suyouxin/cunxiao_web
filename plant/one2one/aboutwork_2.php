    <td colspan="6" valign="top"><p style="margin:0px">&nbsp;</p>
      <p style="margin:0px">当捐助人和学生之间的桥梁，看似就是打打电话，实际上却是个充满琐碎细节，耗工耗时的工作。可能越是分布在社会两极的人，联络难度就越大，从网上查不到盖茨的手机号，同样，那个我昨天才在山沟里见过的，寒暄老半天的学生父亲，今天打电话，他就停机了。他没有邮箱，也@不到他，然后就没有然后了。</p>
      <p style="margin:0px">但捐助人还在等回复，我们就要查查有没在捐的同班同学可以带话，或者曾去过他的邻居家。情况着急，在淘宝上立刻充十块钱话费给他也是一个办法。俗话说：不想做侦探的社工不是好客服。</p>
      <p style="margin:0px">&nbsp;</p>
      <p style="margin:0px">粗略算算，现在捐学生两千，对应的捐助人也就是两千，数量还在增加，质量也有要增加，我们拿起电话就变身小飞侠。</p>
